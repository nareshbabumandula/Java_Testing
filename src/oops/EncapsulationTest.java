package oops;

public class EncapsulationTest {
	
	// To encapsulate a class or to achieve encapsulation at least one data member to be private
    private String USERNAME = "praveen";
	private String PASSWORD = "future123";
	
	public String getUSERNAME() {
		return USERNAME;
	}
	public void setUSERNAME(String uSERNAME) {
		USERNAME = uSERNAME;
	}
	public String getPASSWORD() {
		return PASSWORD;
	}
	public void setPASSWORD(String pASSWORD) {
		PASSWORD = pASSWORD;
	}
	
	


}
